package pos.domain;

public class PaisImpl implements Pais {

	private String id;
	private String descripcion;
	
	public PaisImpl(){
	}

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getDescripcion() {
		return descripcion;
	}

	public void setDescripcion(String descripcion) {
		this.descripcion = descripcion;
	}
	
	
}
